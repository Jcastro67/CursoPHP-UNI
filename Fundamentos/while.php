<?php 
// Bucle While, mientras una condicion sea verdadera se ejecutará la sentencia
$n=1;
while ($n<=20){
    echo $n;
    echo ", ";
    $n++;
}

// Bucle Do While, Realizar una sentencia mientras una condicion sea verdadera
$n1=1;
do {
    echo $n1;
    echo ", ";
    $n1++;
} while ($n1<=20);

?>